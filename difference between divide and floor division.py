#When performing the operation 10/3 the result is 3.3333333333333335. 

#However, when performing the operation 10//3 the result is 3.

#The // operator performs a floor division, which means that it returns the largest possible integer less than or equal to the result of the division. In this case, the result of 10//3 is 3, which is the largest integer less than or equal to 3.3333333333333335.

#However, an example where the // method would get a result equal to the result ofthe regular / is when there is no decimal in the result of the division.
#EG. 4 // 2 = 2
